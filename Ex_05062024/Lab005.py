#Use of type function & demo of Dynamically typed Python language

#Variable name should start with A-Z, a-z
#Variable name should not start with Number
#Variable name should not start with keyword
#Variable name should not start with special character
#Variable name should not start with special space

age = 123
print(type(age))
age = 3.14262
print(type(age))
age = "Mangesh"
print(type(age))
age = True
print(type(age))