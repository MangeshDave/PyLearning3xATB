#Use of List functions append, extend, len

shopping_list = ["milk", "Poha", "bread", "butter"]
print(shopping_list)
print(len(shopping_list))
#Append -> adds one element at the end
shopping_list.append("ghee")
print(shopping_list)
#Extend -> adds another list at the end
shopping_list.extend(["aata", "rice"])
print(shopping_list)