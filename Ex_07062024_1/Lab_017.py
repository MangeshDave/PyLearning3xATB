#Use of List functions insert, remove, pop

shopping_list = ["milk", "Poha", "bread", "butter"]
print(shopping_list)
#Insert -> Insert an element at specified index
shopping_list.insert(1, "sugar")
print(shopping_list)
#Remove -> deletes ONE element you want to remove
shopping_list.remove("milk")
print(shopping_list)
#Pop -> deletes ONE element from last
shopping_list.pop()
print(shopping_list)